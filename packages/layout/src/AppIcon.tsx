/**
 * AppIcon - Renders a monochromatic SVG icon for a DS app
 * 
 * Uses currentColor so icons inherit the text color of their parent.
 * This ensures consistent coloring in light/dark mode and various contexts.
 */
export function AppIcon({ appId, className = '' }: AppIconProps) {
  const normalizedId = appId.toLowerCase();
  const icon = iconComponents[normalizedId] || defaultIcon;
  
  return (
    <span className={`inline-flex items-center justify-center ${className}`}>
      {icon}
    </span>
  );
}

/**
 * Check if an icon string is an emoji (vs custom icon name/URL)
 */
export function isEmojiIcon(icon: string): boolean {
  // Emojis typically have length > 1 when measured in code units
  // and contain characters outside ASCII range
  return icon.length > 0 && icon.charCodeAt(0) > 127;
}
